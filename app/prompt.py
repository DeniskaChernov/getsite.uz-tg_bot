def build_system_prompt(service_name: str | None, lang: str, brief_state: str,
                        client_name: str | None = None, dialog_stage: str | None = None) -> str:
    return SYSTEM_PROMPT_TEMPLATE.format(
        service_name_or_none=service_name or "не указана",
        client_name=client_name or "не известно",
        detected_lang=lang,
        dialog_stage=dialog_stage or "уточнение задачи",
        brief_state=brief_state or "пусто",
    )


def infer_dialog_stage(brief: dict, user_messages: int, awaiting_confirm: bool = False) -> str:
    """Грубая оценка этапа для подсказки модели - без отдельного LLM-вызова."""
    if awaiting_confirm or brief.get("_awaiting_confirm"):
        return "ожидание подтверждения сводки"
    if brief.get("_lead_sent"):
        return "заявка уже подтверждена"
    filled = sum(1 for k in ("service", "niche", "deadline", "budget_hint", "summary") if brief.get(k))
    if user_messages <= 1 and filled == 0:
        return "знакомство / первый контакт"
    if filled < 2:
        return "уточнение задачи и ниши"
    if filled < 4:
        return "сбор деталей брифа"
    return "почти готов к сводке - мягко добрать недостающее"


